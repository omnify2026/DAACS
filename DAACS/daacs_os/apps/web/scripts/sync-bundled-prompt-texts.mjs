import { readFileSync, readdirSync, statSync, writeFileSync, existsSync } from "fs";
import { join, dirname, basename, extname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const webRoot = join(__dirname, "..");
const srcLib = join(webRoot, "src", "lib");
const metaPath = join(srcLib, "defaultBundledAgentsMetadata.ts");
const outPath = join(srcLib, "defaultBundledAgentPromptTexts.ts");

function extractPromptKeysFromMetadata(ts) {
  const set = new Set();
  for (const m of ts.matchAll(/"prompt_key"\s*:\s*"([^"]+)"/g)) {
    set.add(m[1]);
  }
  return [...set];
}

function promptBodyFromJson(raw) {
  let root;
  try {
    root = JSON.parse(raw);
  } catch {
    return "";
  }
  if (root == null || typeof root !== "object" || Array.isArray(root)) return "";
  const c = root.content;
  if (typeof c === "string") return c;
  if (Array.isArray(c)) return c.map((line) => String(line ?? "")).join("\n");
  return "";
}

function resolveSourceDir() {
  const arg = process.argv[2]?.trim();
  if (arg) return arg;
  const env = process.env.DAACS_PROMPTS_SYNC_DIR?.trim();
  if (env) return env;
  if (process.platform === "win32" && process.env.LOCALAPPDATA) {
    const p = join(process.env.LOCALAPPDATA, "DAACS", "Resources", "prompts");
    if (existsSync(p)) return p;
  }
  const desktop = join(webRoot, "..", "desktop", "Resources", "prompts");
  if (existsSync(desktop)) return desktop;
  return null;
}

function main() {
  const metaRaw = readFileSync(metaPath, "utf8");
  const requiredKeys = extractPromptKeysFromMetadata(metaRaw);
  const map = Object.fromEntries(requiredKeys.map((k) => [k, ""]));

  const sourceDir = resolveSourceDir();
  if (sourceDir != null && existsSync(sourceDir) && statSync(sourceDir).isDirectory()) {
    for (const name of readdirSync(sourceDir)) {
      const full = join(sourceDir, name);
      if (!statSync(full).isFile()) continue;
      const ext = extname(name).toLowerCase();
      const stem = basename(name, ext);
      if (stem === "") continue;
      if (ext === ".txt") {
        map[stem] = readFileSync(full, "utf8");
      } else if (ext === ".json") {
        const body = promptBodyFromJson(readFileSync(full, "utf8"));
        if (body !== "" || map[stem] === "") map[stem] = body;
      }
    }
  }

  const lines = [
    "/* Generated by scripts/sync-bundled-prompt-texts.mjs — run: npm run sync-bundled-prompts [promptsDir] */",
    "/* Source: Local AppData DAACS/Resources/prompts or DAACS_PROMPTS_SYNC_DIR or ../desktop/Resources/prompts */",
    "",
    "export const DEFAULT_BUNDLED_PROMPT_TEXT_BY_KEY: Readonly<Record<string, string>> = Object.freeze({",
  ];
  const keys = [...new Set([...Object.keys(map), ...requiredKeys])].sort();
  for (const k of keys) {
    const v = map[k] ?? "";
    lines.push(`  ${JSON.stringify(k)}: ${JSON.stringify(v)},`);
  }
  lines.push("});", "");
  writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`Wrote ${outPath} (${keys.length} keys) from dir: ${sourceDir ?? "(none, empty bodies)"}`);
}

main();
