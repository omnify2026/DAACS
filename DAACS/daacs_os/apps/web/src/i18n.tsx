type I18nValue = {
  locale: Locale;
  setLocale: (next: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

import { STORAGE_KEY_LOCALE } from "./constants";

const I18nContext = createContext<I18nValue | null>(null);
let warnedMissingProvider = false;

function detectLocale(): Locale {
  try {
    if (typeof localStorage !== "undefined") {
      const raw = localStorage.getItem(STORAGE_KEY_LOCALE);
      if (raw === "ko" || raw === "en") return raw;
    }
  } catch {
    // Fall back to the browser language when storage is unavailable.
  }

  const language = typeof navigator !== "undefined" ? navigator.language.toLowerCase() : "";
  return language.startsWith("ko") ? "ko" : "en";
}

function translate(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  const raw = dictionaries[locale][key] ?? dictionaries.en[key] ?? key;
  if (!vars) return raw;
  return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), raw);
}

export function tStatic(key: string, vars?: Record<string, string | number>): string {
  return translate(detectLocale(), key, vars);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => detectLocale());

  useEffect(() => {
    try {
      if (typeof localStorage !== "undefined") {
        localStorage.setItem(STORAGE_KEY_LOCALE, locale);
      }
    } catch {
      // Keep the app usable even when localStorage is blocked.
    }
  }, [locale]);

  const value = useMemo<I18nValue>(() => {
    const t = (key: string, vars?: Record<string, string | number>): string => translate(locale, key, vars);
    return { locale, setLocale, t };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

function createFallbackI18nValue(): I18nValue {
  const locale = detectLocale();
  return {
    locale,
    setLocale: (next: Locale) => {
      try {
        if (typeof localStorage !== "undefined") {
          localStorage.setItem(STORAGE_KEY_LOCALE, next);
        }
      } catch {
        // This is an emergency fallback path, so never crash on storage.
      }
    },
    t: (key: string, vars?: Record<string, string | number>): string => translate(locale, key, vars),
  };
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  const fallback = useMemo(() => createFallbackI18nValue(), []);

  useEffect(() => {
    if (ctx || warnedMissingProvider) return;
    warnedMissingProvider = true;
    console.warn("useI18n was called outside I18nProvider; using fallback locale context.");
  }, [ctx]);

  return ctx ?? fallback;
}
